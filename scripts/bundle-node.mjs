// 把内嵌 Node 入口打成单文件 public/nodejs-project/main.js。
//
// 入口 src/server/nodejsEntry.ts → 经 esbuild 收进它 import 的 src/lib/* 数据层（算法不分叉），
// 产出单文件 CJS，供 @choreruiz/capacitor-node-js 在 App 内以 Node 18 运行。
//
// 关键选项：
//   platform: node, target: node18  —— 内嵌运行时是 nodejs-mobile v18.20.4
//   format: cjs                     —— 入口用 require("bridge")，且插件以 CJS 加载 main
//   external: ["bridge"]            —— bridge 是插件运行时注入的内置模块，不打包
//   bundle: true                    —— 把 src/lib/* 全收进单文件
//
// 用法：node scripts/bundle-node.mjs   （由 npm run bundle:node 调用）

import { build } from "esbuild";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");

await build({
  entryPoints: [resolve(root, "src/server/nodejsEntry.ts")],
  outfile: resolve(root, "public/nodejs-project/main.js"),
  bundle: true,
  platform: "node",
  target: "node18",
  format: "cjs",
  // bridge：插件运行时注入；node:* 内置模块本就 external，esbuild 对 node 平台自动处理。
  external: ["bridge"],
  // nodejs-mobile 无 brotli/部分原生扩展，保持纯 JS；遇到顶层 await 也兼容。
  logLevel: "info",
  legalComments: "none",
  banner: {
    js: "// AUTO-GENERATED by scripts/bundle-node.mjs — 勿手改。源在 src/server + src/lib。",
  },
});

console.log("[gold] bundled → public/nodejs-project/main.js");
